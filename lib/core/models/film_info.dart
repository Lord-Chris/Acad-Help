class FilmInfo {
  final String title;
  final String description;
  final String posterImage;
  final String releaseDate;
  final String actors;
  final String genre;
  final String rating;
  FilmInfo({
    required this.title,
    required this.description,
    required this.posterImage,
    required this.releaseDate,
    required this.actors,
    required this.genre,
    required this.rating,
  });
}
